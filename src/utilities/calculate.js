function add(first, second) {
    return first + second;
}

const multiply = (first, second) => {
    return first * second;
}

const subtract = (first, second) => {
    return first - second;
}

const divide = (first, second) => {
    return first / second;
}

const remainder = (first, second) => {
    return first % second;
}

export { add, multiply, subtract, divide, remainder };